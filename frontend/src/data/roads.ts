function scoreTierFromScore(score: number): RoadSummaryCardProps['scoreTier'] {
  if (score >= 80) return 'excellent'
  if (score >= 70) return 'good'
  if (score >= 55) return 'fair'
  return 'poor'
}

function riskLevelFromScore(score: number): RoadSummaryCardProps['riskLevel'] {
  if (score >= 80) return 'low'
  if (score >= 70) return 'medium'
  if (score >= 55) return 'high'
  return 'critical'
}

